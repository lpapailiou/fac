package parser.parsetree;

/**
 * This is a wrapper class for unary expressions.
 * Its instances will hold an assignment operator and an operand.
 */
public class UnaryExpression extends Expression {

    private final UnOp op;
    private final Object operand;

    /**
     * This constructor will instantiate a wrapper for a binary expression.
     *
     * @param op      the operator.
     * @param operand the operand.
     * @param left    the start index.
     * @param right   the end index.
     */
    UnaryExpression(Object op, Object operand, int left, int right) {
        super(left, right);
        this.op = UnOp.getByLiteral(op);
        this.operand = operand;
    }

    /**
     * Returns the unary operator.
     *
     * @return the operator.
     */
    public UnOp getOperator() {
        return op;
    }

    /**
     * Returns the operand.
     *
     * @return the operand.
     */
    public Object getOperand() {
        return operand;
    }

    /**
     * The toString method provides a pretty-printable String
     * of this parse tree component.
     * It is generated by the contents of this instance and may not be equal to the original code.
     *
     * @return the pretty-printed code.
     */
    @Override
    public String toString() {
        String out = operand.toString();
        if (op == UnOp.DEC || op == UnOp.INC) {
            out += op.getLiteral();
        } else {
            out = op.getLiteral() + out;
        }
        return out;
    }

    /**
     * This method returns the unary expression as representation of the parse tree.
     *
     * @return a snipped of the parse tree.
     */
    @Override
    public String getParseTree() {
        StringBuilder out = getStringBuilder(this);
        if (op != UnOp.DEC && op != UnOp.INC) {
            appendUnOp(out, op, 1);
        }
        appendNestedComponents(out, operand, 1);
        if (op == UnOp.DEC || op == UnOp.INC) {
            appendUnOp(out, op, 1);
        }
        return out.toString();
    }

}